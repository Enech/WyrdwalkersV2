<template>
  <div class="pa-3" id="app-content-custom">
    <v-card max-width="1000" class="mx-auto">
      <v-card-title class="title">Statuts de l'association</v-card-title>
      <v-card-text
        class="subtitle-1"
      >Proposés aux associations déclarées par application de la loi du 1er juillet 1901 et du décret du 16 août 1901.</v-card-text>
      <v-divider></v-divider>
      <v-card-text>
        <article class="mb-10">
          <h3>Article premier - Nom</h3>
          <p>Il est fondé entre les adhérents aux présents statuts une association régie par la loi du 1er juillet 1901 et le décret du 16 août 1901, ayant pour titre : WYRDWALKERS.</p>
        </article>
        <article class="mb-10">
          <h3>Article 2 - But objet</h3>
          <p>Cette association a pour objet de promouvoir l’activité du jeu de rôle et de faire découvrir la mythologie par l’intermédiaire d’activités axées sur l’imaginaire, le ludique et la pédagogie.</p>
        </article>
        <article class="mb-10">
          <h3>Article 3 - Siège social</h3>
          <p>Le siège social est fixé au 3 rue de la Villette, 69003 Lyon.
            <br />Il pourra être transféré par simple décision du conseil d'administration.</p>
        </article>
        <article class="mb-10">
          <h3>Article 4 - Durée</h3>
          <p>La durée de l’association est illimitée.</p>
        </article>
        <article class="mb-10">
          <h3>Article 5 - Composition</h3>
          <p>L'association se compose de :</p>
          <ul>
            <li>Membres d'honneur</li>
            <li>Membres bienfaiteurs</li>
            <li>Membres actifs ou adhérents</li>
            <li>Membres fondateurs</li>
          </ul>
          <p>
            Les membres ainsi désignés sont des personnes physiques ou des personnes morales. En cas de personne morale membre de l’association, celle-ci peut être de droit public ou privé. Il revient à cette personne morale de désigner un représentant physique et
            de tenir informé l’association des éventuels changements de ce représentant.
            <br />Une personne morale ne peut pas être membre du Conseil d’Administration.
            <br />Les membres fondateurs
            possèdent un droit de regard et de veto sur les actions entreprises par le conseil d’administration et le bureau, qu’ils exerceront avec parcimonie et respect des contributions de chacun.
          </p>
        </article>
        <article class="mb-10">
          <h3>Article 6 - Admission</h3>
          <p>
            L’association est ouverte aux personnes considérées comme majeures sur le territoire français. Les personnes mineures devront fournir une autorisation parentale afin de valider leur adhésion.
            <br />Pour faire partie de l'association, il faut être agréé par le conseil d’administration, qui statue, lors de chacune de ses réunions, sur les demandes d'admission présentées.
          </p>
        </article>
        <article class="mb-10">
          <h3>Article 7 - Membres - Cotisations</h3>
          <p>
            Sont membres adhérents ceux qui ont pris l'engagement de verser annuellement une somme de 15€ à titre de cotisation.
            <br />Sont membres d'honneur ceux qui ont rendu des services signalés à l’association ; ils sont dispensés
            de cotisation.
            <br />Sont membres bienfaiteurs, les personnes qui versent un droit d'entrée de 100€ et, au-delà de la première année d’adhésion, une cotisation annuelle fixée chaque année par l'assemblée générale.
            <br />Sont membres fondateurs les créateurs de l’association et créateurs du contenu imaginaire et encyclopédique servant de base au fonctionnement de l’association. Ces membres sont Emile DOUILHET, Sébastien DOUILHET et
            Thomas GELY.
            <br />
            <br />Toute cotisation pourra être rachetée moyennant le paiement d'une somme minima égale au montant de la cotisation versée.
          </p>
        </article>
        <article class="mb-10">
          <h3>Article 8 - Radiations</h3>
          <p>La qualité de membre se perd par :</p>
          <ul>
            <li>La démission</li>
            <li>Le décès</li>
            <li>
              La radiation prononcée par le conseil d'administration pour non-paiement de la cotisation ou pour sanction prononcée, l'intéressé ayant été invité à fournir des explications devant le bureau et/ou par écrit. Le conseil
              d’administration ne peut toutefois pas se prononcer sur la radiation d’un membre fondateur.
            </li>
          </ul>
        </article>
        <article class="mb-10">
          <h3>Article 9 - Affiliations</h3>
          <p>La présente association n’est présentement affiliée à aucune autre association.</p>
        </article>
        <article class="mb-10">
          <h3>Article 10 - Ressources</h3>
          <p>Les ressources de l'association comprennent :</p>
          <ol>
            <li>Le montant des droits d'entrée et des cotisations</li>
            <li>Les subventions de l'Etat, des départements et des communes</li>
            <li>Les ateliers payants proposés par l’association</li>
            <li>Toutes les ressources autorisées par les lois et règlements en vigueur</li>
          </ol>
        </article>
        <article class="mb-10">
          <h3>Article 11 - Assemblée générale ordinaire</h3>
          <p>
            L'assemblée générale ordinaire comprend tous les membres de l'association à quelque titre qu'ils soient.
            <br />Elle se réunit chaque année au mois de Juillet.
            <br />Quinze jours au moins avant la date fixée, les membres de l'association sont convoqués par les soins du secrétaire. L'ordre du jour figure sur les convocations.
            <br />
            <br />Le président, assisté des membres du conseil,
            préside l'assemblée et expose la situation morale ou l’activité de l'association.
            <br />Le trésorier rend compte de sa gestion et soumet les comptes annuels (bilan, compte de résultat et annexe) à l'approbation de l'assemblée.
            <br />L’assemblée générale fixe le montant des cotisations annuelles et du droit d’entrée à verser par les différentes catégories de membres.
            <br />Ne peuvent être abordés que les points inscrits à l'ordre du jour.
            <br />
            <br />Les décisions sont prises à la majorité absolue des voix des membres présents ou représentés.
            <br />Les membres ne pouvant être présents lors de l’assemblée générale et désirant exercer leur droit de vote devront
            désigner un représentant au bureau au minimum 5 jours avant l’assemblée. Ledit représentant devra être muni d’une photocopie d’un titre d’identité du membre représenté ainsi que d’une procuration signée.
            <br />Il est
            procédé, après épuisement de l'ordre du jour, au renouvellement des membres sortants du conseil.
            <br />
            <br />Toutes les délibérations sont prises à main levée, excepté l’élection des membres du conseil.
            <br />Les décisions des assemblées générales s’imposent à tous les membres, y compris absents ou représentés.
            <br />
          </p>
        </article>
        <article class="mb-10">
          <h3>Article 12 - Assemblée générale extraordinaire</h3>
          <p>
            Si besoin est, ou sur la demande de la moitié plus un des membres inscrits, le président peut convoquer une assemblée générale extraordinaire, suivant les modalités prévues aux présents statuts et uniquement pour modification des statuts ou la dissolution.
            <br />Les modalités de convocation sont les mêmes que pour l’assemblée générale ordinaire.
            <br />Les délibérations sont prises à la majorité des membres présents et représentés.
          </p>
        </article>
        <article class="mb-10">
          <h3>Article 13 - Conseil d'administration</h3>
          <p>
            L'association est dirigée par un conseil de 3 à 5 membres, élus pour 1 an par l'assemblée générale. Les membres sont rééligibles.
            <br />
            <br />Le conseil étant renouvelé chaque année par moitié, la première année, les membres sortants sont désignés par tirage au sort.
            <br />
            <br />En cas de vacances, le conseil pourvoit provisoirement au remplacement de ses membres. Il est procédé à leur remplacement définitif par la plus prochaine assemblée générale. Les pouvoirs des membres ainsi élus
            prennent fin à l'expiration du mandat des membres remplacés.
            <br />
            <br />Le conseil d'administration se réunit au moins une fois tous les trois mois, sur convocation du président, ou à la demande du quart de ses membres.
            <br />
            <br />Les décisions sont prises à la majorité des voix ; en cas de partage, les membres fondateurs doivent être consultés.
            <br />
            <br />Tout membre du conseil qui, sans excuse valable, n'aura pas assisté à trois réunions consécutives sera considéré comme démissionnaire.
          </p>
        </article>
        <article class="mb-10">
          <h3>Article 14 - Bureau</h3>
          <p>Les ressources de l'association comprennent :</p>
          <ol>
            <li>Un-e président-e</li>
            <li>Un-e- secrétaire et, s'il y a lieu, un-e secrétaire adjoint-e</li>
            <li>Un-e trésorier-e, et, si besoin est, un-e trésorier-e adjoint-e</li>
          </ol>
          <p>Les fonctions de trésorier-e et de président-e ne sont pas cumulables.</p>
        </article>
        <article class="mb-10">
          <h3>Article 15 - Indemnités</h3>
          <p>Toutes les fonctions, y compris celles des membres du conseil d’administration et du bureau, sont gratuites et bénévoles. Seuls les frais occasionnés par l’accomplissement de leur mandat sont remboursés sur justificatifs. Le rapport financier présenté à l’assemblée générale ordinaire présente, par bénéficiaire, les remboursements de frais de mission, de déplacement ou de représentation.</p>
        </article>
        <article class="mb-10">
          <h3>Article 16 - Réglement intérieur</h3>
          <p>Lors de sa création, l’association WYRDWALKERS sera régie par le règlement intérieur suivant :</p>
          <ol>
            <li>
              L’association WYRDWALKERS est une association ayant pour but la diffusion des connaissances en histoire et en mythologie par l’intermédiaire du jeu de rôle et des ateliers pédagogiques. A ce titre, l’association ne
              possède aucun caractère religieux ni aucune affiliation religieuse d’aucune sorte. Les membres de l’association sont donc invités à exprimer leurs opinions philosophiques et/ou religieuses dans les limites de la
              bienséance et l’écoute commune. De même, l’association WYRDWALKERS n’est lié à aucune entité ou courant politique.
            </li>
            <li>Les droits d’entrée sont exigibles dès l’agrément des nouveaux membres, pour le montant total d’une année entière. Toute cotisation payée est définitivement acquise à l’association.</li>
            <li>Tout membre de l’association se doit de participer à son bon fonctionnement.</li>
            <li>
              Dans le cas d’évènements payants organisés par l’association, tous ses membres sans distinction s’y voient attribués un tarif préférentiel pouvant aller jusqu’à la gratuité. Ces évènements payants sont toujours gratuits
              pour les membres fondateurs et bienfaiteurs.
            </li>
            <li>Le conseil d’administration doit comporter 3 membres au minimum, à savoir les membres du bureau.</li>
            <li>
              Le conseil d’administration comporte deux postes optionnels : un responsable communication extérieure et un responsable logistique. Si ces postes ne sont pas pourvus, leurs responsabilités sont distribuées équitablement
              entre les membres du bureau et consignées à la fin de chaque assemblée générale.
            </li>
            <li>Le président de l’association en est le représentant légal. Il est en charge de la tenue administrative de l’association.</li>
            <li>Le secrétaire est en charge de la collecte des informations à transmettre aux membres de l’association ainsi que de la communication desdites informations.</li>
            <li>
              Le trésorier est en charge de la tenue des finances de l’association. Il est le contact privilégié de l’établissement bancaire hébergeant le compte de l’association. Il est aussi la personne à contacter pour la gestion
              des indemnités.
            </li>
            <li>Le responsable communication extérieure est en charge de la diffusion des informations relatives à la vie de l’association au grand public, ainsi que de la tenue d’une présence de l’association sur les réseaux sociaux.</li>
            <li>Le responsable logistique est en charge de la gestion pratique des évènements et des besoins matériels de l’association.</li>
            <li>Seuls les membres du bureau et les membres fondateurs possèdent les procurations nécessaires à la dépense des ressources financières de l’association.</li>
            <li>Tout contact au nom de l’association WYRDWALKERS auprès d’autres entités doit être reportée aux membres du conseil d’administration.</li>
            <li>Toute infraction au présent règlement intérieur fera l’objet d’une délibération auprès du conseil d’administration à qui incombe la responsabilité d’éventuelles sanctions.</li>
          </ol>
          <p>Ce règlement intérieur est révisable lors des assemblées générales et réunions du conseil d’administration. Tout changement au règlement décrit ci-dessus doit être répertorié par le ou la secrétaire et transmise par voie officielle (électronique ou physique) aux membres de l’association.</p>
        </article>
        <article class="mb-10">
          <h3>Article 17 - Dissolution</h3>
          <p>En cas de dissolution prononcée selon les modalités prévues à l’article 12, un ou plusieurs liquidateurs sont nommés, et l'actif net, s'il y a lieu, est dévolu à un organisme ayant un but non lucratif conformément aux décisions de l’assemblée générale extraordinaire qui statue sur la dissolution. L’actif net ne peut être dévolu à un membre de l’association, même partiellement, sauf reprise d’un apport.</p>
        </article>
        <article class="mb-10">
          <h3>Article 18 - Libéralités</h3>
          <p>Le rapport et les comptes annuels, tels que définis à l’article 11 (y compris ceux des comités locaux) sont adressés chaque année au Préfet du département. L'association s'engage à présenter ses registres et pièces de comptabilité sur toute réquisition des autorités administratives en ce qui concerne l'emploi des libéralités qu'elle serait autorisée à recevoir, à laisser visiter ses établissements par les représentants de ces autorités compétents et à leur rendre compte du fonctionnement desdits établissements.</p>
        </article>
